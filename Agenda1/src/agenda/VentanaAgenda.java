    private void mostrar (ArrayList <Contacto> ag)
    {
        String matriz[][]=new String[contactos.size()][5];
        for (int i = 0;i < ag.size();i++)
        {
            matriz[i][0]= ag.get(i).getNombre();
            matriz[i][1]= ag.get(i).getTelefono();
            matriz[i][2]= ag.get(i).getDireccion();
            matriz[i][3]= ag.get(i).getCorreo();
            matriz[i][4]= ag.get(i).getAlias();
            
        }
        Tabla.setModel(new javax.swing.table.DefaultTableModel(
            matriz,
            new String [] {
                "Nombre", "Teléfono", "Dirección", "E-mail", "Alias"
            }
        ));
    }
    
