    private ArrayList<Contacto> buscar(String tbuscar){
        for (Contacto contacto : contactos) {
            if (contacto.getNombre().contains(tbuscar) || contacto.getTelefono().contains(tbuscar)||
                    contacto.getDireccion().contains(tbuscar) || contacto.getCorreo().contains(tbuscar) ||
                    contacto.getAlias().contains(tbuscar)) {
                    auxbuscar.add(contacto);
            }
        }
        return auxbuscar;
    }
